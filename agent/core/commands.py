from agent.core.reasoning import reason
from agent.llm import ask
from agent.core.prompt import SYSTEM_PROMPT


def analyze(query: str):

    plan = reason(query)

    files = plan["context"]

    if not files:
        return "Ничего не найдено."

    context = ""

    for file in files:

        context += f"""

========================================================
ПРОЕКТ : {file["project"]}
ФАЙЛ   : {file["path"]}
========================================================

{file["content"]}

"""

    prompt = f"""
{SYSTEM_PROMPT}

==================================================
ПЛАН АНАЛИЗА
==================================================

Тип запроса:
{plan["intent"]}

Запрос:
{plan["query"]}

Главный символ:
{plan["symbol"]}

Главный файл:
{plan["file"]}

Граф зависимостей:
{plan["graph"]}

==================================================
КОНТЕКСТ ПРОЕКТА
==================================================

{context}

==================================================

Ты инженер ENRG AI.

Работай ТОЛЬКО с предоставленным контекстом.

Не придумывай файлы, функции или архитектуру.

Если информации недостаточно — прямо сообщи об этом.

Ответь строго на русском языке.

Структура ответа:

1. Что найдено.
2. Назначение.
3. Как это работает.
4. Связанные компоненты.
5. Какие файлы будут затронуты при изменении.
6. Возможные риски.
7. Рекомендации.
"""

    return ask(prompt)
